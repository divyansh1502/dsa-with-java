
public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1194f;
        System.out.printf("Formatted number is %.2f\n",a);
        System.out.printf("Pie: %.3f\n", Math.PI);
        System.out.printf("Hello my name is %s  an I am %s", "Divyansh","Cool");
    }
}
// Common Java format specifiers (placeholders):
// %d → integer numbers
// %f → floating-point numbers
// %c → single character
// %s → string values
// %b → boolean values (true/false)
